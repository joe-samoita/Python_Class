#  LOGICAL OPERATORS

num1 = 44
num2 = 55

print(num1 < num2 and num2 < num1)  # if all the conditions is true returns true
print(num1 < num2 or num2 < num1)   # if one of the conditions is true returns true
print(not (num1 < num2 and num2 < num1))  # returns the opposite of the result